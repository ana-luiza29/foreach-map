/*   
Questão 17 - Qual é a diferença entre map() e forEach() em termos de 
manipulação do array original?  
 
*/

/*
resposta
 A diferença entre map e forEach em termos de manipulação do array original é que map cria e retorna um novo array com os resultados da aplicação da função de callback a cada elemento, enquanto forEach apenas itera sobre os elementos sem modificar o array original.
 */